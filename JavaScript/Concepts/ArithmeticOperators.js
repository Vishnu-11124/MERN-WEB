
// Arithmetic operators are used to perform mathematical operations on numbers.
// The basic arithmetic operators in JavaScript include addition, subtraction, multiplication, division, modulus, and exponentiation.

// 1. Addition (+)
let sum = 10 + 5; // Output: 15

// 2. Subtraction (-)
let difference = 10 - 5; // Output: 5

// 3. Multiplication (*)
let product = 10 * 5; // Output: 50

// 4. Division (/)
let quotient = 10 / 5; // Output: 2

// 5. Modulus (%)
let remainder = 10 % 3; // Output: 1

// 6. Exponentiation (**)
let power = 2 ** 3; // Output: 8

// 7. Increment (++)
let count = 5;
count++; // Output: 6

// 8. Decrement (--)
let number = 5;
number--; // Output: 4

