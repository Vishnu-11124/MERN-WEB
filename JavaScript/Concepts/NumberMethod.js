
// 1. toFixed()
// The toFixed() method formats a number using fixed-point notation.

let amount = 1234.5678;
console.log(amount.toFixed(2)); // Output: "1234.57"

// 2. isNaN()
// The isNaN() function determines whether a value is NaN (Not-a-Number

let value1 = "50";
console.log(isNaN(value1)); // Output: true

// 3. parseInt()
// The parseInt() function parses a string argument and returns an integer of the specified radix (base).

let strNumber = "100px";
console.log(parseInt(strNumber)); // Output: 100

// 4. parseFloat()
// The parseFloat() function parses a string argument and returns a floating-point number.

let strFloat = "10.5px";
console.log(parseFloat(strFloat)); // Output: 10.5
