// For of loop is used to iterate over iterable objects like arrays, strings, maps, sets, etc.

// for (let variable of iterable) {
//     // code to be executed
// }

const numbers = [10, 20, 30, 40, 50];

for (let num of numbers) {
    console.log(num);
}

// Output:
// 10
// 20
// 30
// 40
// 50

const str = "Hello";

for (let char of str) {
    console.log(char);
}

// Output:
// H
// e
// l
// l
// o