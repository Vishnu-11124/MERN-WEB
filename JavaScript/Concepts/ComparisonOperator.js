
// Comparison operators are used to compare two values and return a boolean result (true or false).

// 1. Equal to (==)
let a = 5;
let b = '5';
console.log(a == b); // Output: true (loose equality, type coercion occurs)

// 2. Strictly Equal to (===)
console.log(a === b); // Output: false (strict equality, no type coercion)

// 3. Not Equal to (!=)
console.log(a != b); // Output: false (loose inequality, type coercion occurs)

// 4. Strictly Not Equal to (!==)
console.log(a !== b); // Output: true (strict inequality, no type coercion)

// 5. Greater than (>)
console.log(a > 3); // Output: true 

// 6. Less than (<)
console.log(a < 10); // Output: true

// 7. Greater than or Equal to (>=)
console.log(a >= 5); // Output: true

// 8. Less than or Equal to (<=)
console.log(a <= 5); // Output: true

