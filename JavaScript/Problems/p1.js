// Number calculation challenge

let firstNumber = 10;
let secondNumber = 20;

// addition
let sum = firstNumber + secondNumber;
console.log(`The sum of ${firstNumber} and ${secondNumber} is ${sum}`);

// subtraction
let difference = secondNumber - firstNumber;
console.log(`The difference when ${firstNumber} is subtracted from ${secondNumber} is ${difference}`);

// multiplication
let product = firstNumber * secondNumber;
console.log(`The product of ${firstNumber} and ${secondNumber} is ${product}`);

// division
let quotient = secondNumber / firstNumber;
console.log(`The quotient when ${secondNumber} is divided by ${firstNumber} is ${quotient}`);

// modulus
let remainder = secondNumber % firstNumber;
console.log(`The remainder when ${secondNumber} is divided by ${firstNumber} is ${remainder}`);

// power
let power = firstNumber ** 2;
console.log(`${firstNumber} raised to the power of 2 is ${power}`);