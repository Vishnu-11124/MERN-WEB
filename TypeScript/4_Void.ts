// Void - It is a type that represents the absence of any value. It is often used as the return type for functions that do not return a value.


let userInfo = (uname: string, age: number) : void => {
    console.log(`My name is ${uname} and I am ${age} years old.`)
}

userInfo('Sarah', 32) // Output: My name is Sarah and I am 32 years old.

