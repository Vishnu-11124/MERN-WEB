// Check if a character is a lowercase letter, uppercase letter, number, or special character

let char = 'g';

if(islowercase(char)){
    console.log('Lowercase Letter');
}else if(isuppercase(char)){
    console.log('Uppercase Letter');
}else if(isNumber(char)){
    console.log('Number');
}else{
    console.log('Special Character');
}